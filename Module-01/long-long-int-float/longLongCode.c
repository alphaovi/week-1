#include <stdio.h>
int main ()
{

// integer
    int a = 100000000;
    printf("%d\n", a);

    // long long integer
    long long int b = 1000000000000;
    printf("%lld \n", b);

    // float
    float c = 2.123456789123456;
    printf("%f\n", c);

    // double
    double d = 2.123456789123456;
    printf("%0.15lf", d);


    return 0;
}